# ===========================================================================
# STIMELA STEP:  calibrate_phase   (cab: casa.gaincal)
#   params: gaintype=G, calmode=p, solint=10s, minsnr=1e-10,
#           gaintable=[tart.G0a]   (G0a applied on the fly)
#           caltable = caltables/tart.G0p
# ---------------------------------------------------------------------------
# The phase solve runs with the amplitude table applied on the fly: divide DATA
# by (G0a_p * G0a_q), StefCal again, and keep only the PHASE (calmode='p').
# Splitting bounded amplitude then phase is the same discipline the Aligned
# notebook arrived at: phases set source positions; amplitudes are normalised
# so they cannot amplify noise.
# ===========================================================================
amp_pq = np.where(ant_good[ANT1] & ant_good[ANT2], G0a[ANT1] * G0a[ANT2], 1.0)
DATA_ampcal = np.where(amp_pq > 0, DATA / np.where(amp_pq > 0, amp_pq, 1.0), DATA)

g_phase = stefcal(DATA_ampcal, MODEL, ANT1, ANT2, N_ANT, ant_good)
G0p = np.exp(1j * np.angle(g_phase))                    # calmode='p' -> phase only
