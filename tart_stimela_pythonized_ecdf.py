# STIMELA STEP:  applycal   (cab: casa.applycal)
#   params: gaintable=[tart.G0a, tart.G0p], flagbackup=true
# ---------------------------------------------------------------------------
# applycal writes CORRECTED_DATA = DATA / (G_p conj(G_q)). CAL_SOURCE selects
# the gains:
#   'solved'    -> G_p = G0a_p * G0p_p          (the StefCal tables above)
#   'api-phase' -> G_p = exp(i * phase_api_p)    (phase only; the proven path)
# Flagged baselines (dead antennas) are dropped, never divided by ~0.
# ===========================================================================
if CAL_SOURCE == "solved":
    G = G0a * G0p                                       # complex per-antenna gain
elif CAL_SOURCE == "api-phase":
    G = np.where(ant_good, np.exp(1j * gain_phase), 0.0)
else:
    raise ValueError(CAL_SOURCE)

denom = G[ANT1] * np.conj(G[ANT2])
valid = (~FLAG) & (np.abs(denom) > 0)
CORRECTED = np.zeros_like(DATA)
CORRECTED[valid] = DATA[valid] / denom[valid]
ms["CORRECTED"] = CORRECTED
print("applycal: CAL_SOURCE='" + CAL_SOURCE + "' ->", int(valid.sum()),
      "corrected baselines (", int((~valid).sum()), "dropped ).")

# ===========================================================================
# STIMELA STEP:  snapshotimage (part 1)  ->  wsclean `weight briggs 0.0`
# ---------------------------------------------------------------------------
# wsclean weights each visibility before imaging. Briggs/robust weighting
# interpolates between natural (robust=+2) and uniform (robust=-2). We grid the
# (u, v) samples (plus conjugates) to estimate the local density W_k per cell,
# then form  w = 1 / (1 + W_k * f2),  with
#     f2 = (5 * 10^-robust)^2 / ( sum W_k^2 / sum W_k ).
# TART's uv-coverage is sparse and non-redundant, so most cells hold a single
# sample and briggs/uniform/natural nearly coincide -- the weighting is applied
# for fidelity to wsclean rather than for a large change in the map.
# ===========================================================================
Uc, Vc, Vis = U[valid], V[valid], CORRECTED[valid]
uvmax = np.hypot(Uc, Vc).max()
ncell = 256
def cell_idx(u, v):
    iu = np.clip(((u + uvmax) / (2 * uvmax) * (ncell - 1)).astype(int), 0, ncell - 1)
    iv = np.clip(((v + uvmax) / (2 * uvmax) * (ncell - 1)).astype(int), 0, ncell - 1)
    return iu, iv
grid = np.zeros((ncell, ncell))
for (uu, vv) in [(Uc, Vc), (-Uc, -Vc)]:
    iu, iv = cell_idx(uu, vv)
    np.add.at(grid, (iu, iv), 1.0)
iu, iv = cell_idx(Uc, Vc)
Wk = grid[iu, iv]                                       # natural density at each sample
f2 = (5.0 * 10 ** (-ROBUST)) ** 2 / (np.sum(grid ** 2) / np.sum(grid))
Wbriggs = 1.0 / (1.0 + Wk * f2)
Wbriggs = Wbriggs / Wbriggs.sum()                       # normalise
print("Imaging", len(Vis), "weighted visibilities; scheme:", WEIGHTING,
      "robust", ROBUST)

# ===========================================================================
# STIMELA STEP:  snapshotimage (part 2)  ->  wsclean dirty image + PSF
# ---------------------------------------------------------------------------
# wsclean grids + FFTs the weighted visibilities to make the dirty image and
# the PSF (synthesised beam). Because the IUB array is coplanar (w = 0) we
# evaluate the measurement equation EXACTLY with a direct DFT on the (l, m)
# unit disk -- no gridding, no FFT axis ambiguity:
#     I(l, m) = sum_bl  w_bl * Re( V_bl * exp(+2*pi*i (u l + v m)) )
# (Same imager as the Aligned notebook, now visibility-weighted.)
# ===========================================================================
ax = np.linspace(-1.0, 1.0, NG)
Lg, Mg = np.meshgrid(ax, ax)
disk = (Lg ** 2 + Mg ** 2) <= 1.0

def dft_image(vis, wts):
    img = np.zeros(Lg.shape)
    for k in range(len(vis)):
        img += np.real(vis[k] * wts[k] * np.exp(2j * np.pi * (Uc[k] * Lg + Vc[k] * Mg)))
    img[~disk] = 0.0
    return img

dirty = dft_image(Vis, Wbriggs)
psf   = dft_image(np.ones_like(Vis), Wbriggs)
psf   = psf / psf.max()                                 # normalise PSF peak to 1
print("Dirty image:", NG, "x", NG, " peak =", round(float(dirty.max()), 4))

# ===========================================================================
# STIMELA STEP:  snapshotimage (part 3)  ->  wsclean deconvolution + restore
#   wsclean params: niter=5000, gain=0.1, mgain=0.95, auto-mask=5, restore on
# ---------------------------------------------------------------------------
# wsclean runs a Cotton-Schwab CLEAN: minor cycles (Hogbom) subtract scaled
# PSFs from the residual IMAGE down to mgain of the cycle peak; a major cycle
# then predicts the model visibilities, subtracts them from the data, and
# re-images the residual EXACTLY (our DFT) to clear approximation error.
# auto-mask stops cleaning at auto_mask * sigma. Finally the CLEAN model is
# convolved with a fitted Gaussian restoring beam and the residual added back.
# ===========================================================================
def robust_sigma(img):
    v = img[disk]
    return 1.4826 * np.median(np.abs(v - np.median(v)))

def degrid(model_img):
    ys, xs = np.where(model_img != 0.0)
    if len(xs) == 0:
        return np.zeros(len(Vis), dtype=complex)
    lk, mk, fk = Lg[ys, xs], Mg[ys, xs], model_img[ys, xs]
    Vm = np.zeros(len(Vis), dtype=complex)
    for n in range(len(fk)):
        Vm += fk[n] * np.exp(-2j * np.pi * (Uc * lk[n] + Vc * mk[n]))
    return Vm

cy = cx = NG // 2
sigma0 = robust_sigma(dirty)
model = np.zeros_like(dirty)
residual = dirty.copy()
n_major, n_minor, total = 8, 1000, 0
for major in range(n_major):
    cyc_peak = np.abs(residual).max()
    #stop = max(cyc_peak * (1.0 - MGAIN), AUTO_MASK * sigma0)
    stop = cyc_peak * 0.1  # Clean down to 10% of the brightest peak
    for it in range(n_minor):
        if total >= NITER:
            break
        idx = np.unravel_index(np.argmax(np.abs(residual)), residual.shape)
        pk = residual[idx]
        if abs(pk) <= stop:
            break
        amp = pk * GAIN
        model[idx] += amp
        residual -= amp * np.roll(np.roll(psf, idx[0] - cy, 0), idx[1] - cx, 1)
        total += 1
    # ---- major cycle: exact residual from the visibilities (Cotton-Schwab) --
    residual = dft_image(Vis - degrid(model), Wbriggs)
    if np.abs(residual).max() <= AUTO_MASK * sigma0 or total >= NITER:
        break
print("CLEAN:", total, "minor iterations over", major + 1, "major cycles.")

# ---- fit an elliptical Gaussian restoring beam to the PSF main lobe --------
win = 40
sub = psf[cy - win:cy + win, cx - win:cx + win]
yy, xx = np.mgrid[-win:win, -win:win]
mlobe = sub >= 0.5
wsum = sub[mlobe].sum()
sxx = (sub[mlobe] * xx[mlobe] ** 2).sum() / wsum
syy = (sub[mlobe] * yy[mlobe] ** 2).sum() / wsum
sxy = (sub[mlobe] * xx[mlobe] * yy[mlobe]).sum() / wsum
cov = np.array([[sxx, sxy], [sxy, syy]])
if mlobe.sum() < 6 or np.linalg.det(cov) <= 0:
    beam = np.exp(-0.5 * (xx * xx + yy * yy) / 9.0)     # circular fallback
else:
    inv = np.linalg.inv(cov)
    beam = np.exp(-0.5 * (xx * xx * inv[0, 0] + 2 * xx * yy * inv[0, 1] + yy * yy * inv[1, 1]))
beam_full = np.zeros_like(dirty)
beam_full[cy - win:cy + win, cx - win:cx + win] = beam

def fftconv(a, b):
    return np.real(np.fft.ifft2(np.fft.fft2(a) * np.fft.fft2(np.fft.ifftshift(b))))

restored = fftconv(model, beam_full) + residual
restored[~disk] = 0.0
print("Restored image peak =", round(float(restored.max()), 4))

