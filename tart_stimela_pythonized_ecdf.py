# STIMELA STEP:  calibrate_amplitude   (cab: casa.gaincal)
#   params: gaintype=G, calmode=a, solnorm=true, solint=300s, minsnr=1e-10
#           caltable = caltables/tart.G0a
# ---------------------------------------------------------------------------
# gaincal solves per-antenna complex gains g_p from
#     DATA_pq = g_p * conj(g_q) * MODEL_pq           (antenna-based least squares)
# We implement the StefCal alternating-least-squares solver (Salvini &
# Wijnholds 2014). calmode='a' keeps only the AMPLITUDE; solnorm=true
# normalises the mean amplitude to 1 -- this is exactly what stops a low-gain
# antenna from blowing up, the failure mode diagnosed in the Aligned notebook.
# solint=300s over a single snapshot -> one solution per antenna.
#
# NOTE: the Aligned notebook only *applies* gains from the API; it never solves
# for them. This cell is the solve step Stimela does and the Aligned notebook
# omits.
# ===========================================================================
def stefcal(d, m, a1, a2, n_ant, good, n_iter=100, tol=1e-8):
    # solve d_pq = g_p conj(g_q) m_pq for complex g (uses each baseline + conjugate)
    p  = np.concatenate([a1, a2]);     q  = np.concatenate([a2, a1])
    dd = np.concatenate([d, np.conj(d)]); mm = np.concatenate([m, np.conj(m)])
    ok = good[p] & good[q] & (np.abs(mm) > 0)
    p, q, dd, mm = p[ok], q[ok], dd[ok], mm[ok]
    g = np.ones(n_ant, dtype=complex)
    for _ in range(n_iter):
        z   = np.conj(g[q]) * mm                       # predictor for g_p
        num = np.zeros(n_ant, dtype=complex)
        den = np.zeros(n_ant)
        np.add.at(num, p, dd * np.conj(z))
        np.add.at(den, p, (z * np.conj(z)).real)
        gnew = np.where(den > 0, num / np.where(den > 0, den, 1.0), g)
        gnew[~good] = 0.0
        gmix = 0.5 * (g + gnew)                         # damped (even) StefCal step
        if np.linalg.norm(gmix - g) <= tol * max(np.linalg.norm(gmix), 1e-12):
            g = gmix; break
        g = gmix
    return g

